# 3) Ambipolar radial electric field: fluxes and the J_r(E_r) = 0 root
# ---------------------------------------------------------------------------
_AMBIPOLAR_DECK = """! Er-scan deck generated by docs/figures/generate_docs_figures.py
&general
/
&geometryParameters
  geometryScheme = 1
  inputRadialCoordinate = 3
  rN_wish = 0.3
  B0OverBBar = 1.0
  GHat = 1.0
  IHat = 0.0
  iota = 1.31
  epsilon_t = 0.1
  epsilon_h = 0.05
  helicity_l = 2
  helicity_n = 5
  psiAHat = 0.045
  aHat = 0.1
/
&speciesParameters
  Zs = 1 -1
  mHats = 1.0 0.000545509
  nHats = 1.0 1.0
  THats = 1.0 1.0
  dNHatdrHats = -0.5 -0.5
  dTHatdrHats = -1.0 -1.0
/
&physicsParameters
  Delta = 4.5694d-3
  alpha = 1.0
  nu_n = 8.4774d-3
  Er = {er}
  collisionOperator = 1
  includeXDotTerm = .false.
  includeElectricFieldTermInXiDot = .false.
  useDKESExBDrift = .true.
  includePhi1 = .false.
/
&resolutionParameters
  Ntheta = {n_theta}
  Nzeta = {n_zeta}
  Nxi = {n_xi}
  NL = 4
  Nx = {n_x}
  solverTolerance = 1d-10
/
&otherNumericalParameters
  Nxi_for_x_option = 0
/
"""


def ambipolar_er_roots(path: Path, work_dir: Path) -> None:
    from dkx import er as er_solver

    er_bracket = (-3.0, 1.0)
    er_scan = np.linspace(*er_bracket, 5 if TINY else 7)
    n_theta = 7 if TINY else 9
    n_zeta = 7 if TINY else 9
    n_xi = 8 if TINY else 12
    n_x = 3 if TINY else 4

    def write_deck(er: float) -> Path:
        deck = work_dir / "ambipolar_docs.input.namelist"
        deck.write_text(
            _AMBIPOLAR_DECK.format(er=er, n_theta=n_theta, n_zeta=n_zeta, n_xi=n_xi, n_x=n_x)
        )
        return deck

    problem = er_solver.prepare(write_deck(0.0), er_bracket=er_bracket)
    scan_gamma, scan_jr, warm = [], [], None
    for er in er_scan:
        j_r, gamma, state = er_solver.radial_current(
            problem, float(er),
            x0=(warm.x if warm else None), recycle=(warm.recycle if warm else None),
        )
        warm = state
        scan_gamma.append(np.asarray(gamma, dtype=np.float64))
        scan_jr.append(float(j_r))
    scan_gamma = np.asarray(scan_gamma)
    scan_jr = np.asarray(scan_jr)

    result = er_solver.find_ambipolar_er(write_deck(0.0), er_bracket=er_bracket, max_iter=20, emit=None)
    er_root = float(result.er)
    print(f"  ambipolar root: Er={er_root:.4f}  ({result.root_type} root, J_r={result.radial_current:.2e})")

    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(9.2, 3.7))
    ax1.plot(er_scan, scan_gamma[:, 0], "o-", color=BLUE, label=r"$\Gamma_i$ (ions)")
    ax1.plot(er_scan, scan_gamma[:, 1], "s-", color=ORANGE, label=r"$\Gamma_e$ (electrons)")
    ax1.axvline(er_root, color=INK, ls="--", lw=1, label=rf"ambipolar $E_r={er_root:.2f}$")
    ax1.set_xlabel(r"$E_r$  (normalized deck units)", fontsize=9.5, color=INK_2)
    ax1.set_ylabel(r"$\Gamma_s$  (particleFlux_vm_psiHat)", fontsize=9.5, color=INK_2)
    ax1.set_title("Species radial fluxes vs $E_r$", fontsize=9.5, color=INK)
    ax1.grid(True, color=GRID, linewidth=0.7)
    _style_axes(ax1)
    ax1.legend(fontsize=8.5, frameon=False)

    ax2.plot(er_scan, scan_jr, "o-", color=GREEN)
    ax2.axhline(0.0, color=INK_2, lw=0.9)
    ax2.plot([er_root], [result.radial_current], "*", color=INK, ms=15,
             label=rf"{result.root_type} root $E_r={er_root:.2f}$")
    ax2.set_xlabel(r"$E_r$", fontsize=9.5, color=INK_2)
    ax2.set_ylabel(r"$J_r = \sum_s Z_s\,\Gamma_s$", fontsize=9.5, color=INK_2)
    ax2.set_title("Radial current and its ambipolar root", fontsize=9.5, color=INK)
    ax2.grid(True, color=GRID, linewidth=0.7)
    _style_axes(ax2)
    ax2.legend(fontsize=8.5, frameon=False)
    fig.tight_layout()
    fig.savefig(path, dpi=130, bbox_inches="tight", facecolor="white")
    plt.close(fig)


def main() -> None:
    import tempfile

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    with tempfile.TemporaryDirectory(prefix="dkx_docs_figs_") as tmp:
        work_dir = Path(tmp)

        print("[1/3] |B| contour (W7-X, geometryScheme 4)")
        bmag_path = OUT_DIR / "bmag_contour_w7x.png"
        bmag_contour_w7x(bmag_path)
        _report(bmag_path)

        print("[2/3] transport coefficients vs collisionality (RHSMode 3)")
        transport_path = OUT_DIR / "transport_coeff_vs_collisionality.png"
        transport_coeff_vs_collisionality(transport_path, work_dir)
        _report(transport_path)

        print("[3/3] ambipolar E_r roots (J_r = sum_s Z_s Gamma_s = 0)")
        ambipolar_path = OUT_DIR / "ambipolar_er_roots.png"
        ambipolar_er_roots(ambipolar_path, work_dir)
        _report(ambipolar_path)

    print("done: 3 documentation figures under docs/_static/figures/docs/")


if __name__ == "__main__":
    main()
